  display_order INTEGER NOT NULL,
  created_at TIMESTAMP DEFAULT NOW()
);

CREATE INDEX IF NOT EXISTS idx_chat_content_block_message ON chat_content_block(message_id);

-- Track per-step reactions (likes/dislikes) per user and aggregated counts
CREATE TABLE IF NOT EXISTS step_reaction_user (
  id SERIAL PRIMARY KEY,
  user_id INTEGER REFERENCES users(id) ON DELETE CASCADE,
  recipe_id INTEGER NOT NULL REFERENCES recipe_master(id) ON DELETE CASCADE,
  step_index INTEGER NOT NULL,
  reaction TEXT NOT NULL CHECK (reaction IN ('like','dislike')),
  created_at TIMESTAMP DEFAULT NOW(),
  UNIQUE (user_id, recipe_id, step_index)
);

CREATE TABLE IF NOT EXISTS step_reaction_count (
  recipe_id INTEGER NOT NULL REFERENCES recipe_master(id) ON DELETE CASCADE,
  step_index INTEGER NOT NULL,
  likes INTEGER DEFAULT 0,
  dislikes INTEGER DEFAULT 0,
  PRIMARY KEY (recipe_id, step_index)
);

-- Recipe-level reactions (single like/dislike per user, aggregated counts)
CREATE TABLE IF NOT EXISTS recipe_reaction_user (
  id SERIAL PRIMARY KEY,
  user_id INTEGER REFERENCES users(id) ON DELETE CASCADE,
  recipe_id INTEGER NOT NULL REFERENCES recipe_master(id) ON DELETE CASCADE,
  reaction TEXT NOT NULL CHECK (reaction IN ('like','dislike')),
  created_at TIMESTAMP DEFAULT NOW(),
  UNIQUE (user_id, recipe_id)
);

-- Optional link to chat session for context when reaction originates from a chat
ALTER TABLE IF EXISTS recipe_reaction_user
  ADD COLUMN IF NOT EXISTS chat_id INTEGER REFERENCES chat_session(id) ON DELETE SET NULL;

CREATE TABLE IF NOT EXISTS recipe_reaction_count (
  recipe_id INTEGER NOT NULL REFERENCES recipe_master(id) ON DELETE CASCADE PRIMARY KEY,
  likes INTEGER DEFAULT 0,
  dislikes INTEGER DEFAULT 0
);

-- Recipe reviews & ratings (one per user per recipe, upsertable)
CREATE TABLE IF NOT EXISTS recipe_reviews (
  id          SERIAL PRIMARY KEY,
  user_id     INTEGER REFERENCES users(id) ON DELETE CASCADE,
  recipe_id   INTEGER NOT NULL REFERENCES recipe_master(id) ON DELETE CASCADE,
  rating      INTEGER NOT NULL CHECK (rating BETWEEN 1 AND 5),
  review_text TEXT,
  created_at  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
  updated_at  TIMESTAMPTZ NOT NULL DEFAULT NOW(),
