-- Content blocks per message (text, recipe, video, ad, cta)
CREATE TABLE IF NOT EXISTS chat_content_block (
  id SERIAL PRIMARY KEY,
  message_id INTEGER NOT NULL REFERENCES chat_message(id) ON DELETE CASCADE,
  block_type TEXT NOT NULL CHECK (block_type IN ('text', 'recipe', 'video', 'ad', 'cta')),
